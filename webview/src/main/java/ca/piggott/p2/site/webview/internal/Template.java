package ca.piggott.p2.site.webview.internal;

public class Template {

	private String jsDojo;

	private String jsTree;

	private String links;

	private String styleCss;

	private String styleDojo;

	public String getJsDojo() {
		return jsDojo;
	}

	public String getJsTree() {
		return jsTree;
	}

	public String getLinks() {
		return links;
	}

	public String getStyleCss() {
		return styleCss;
	}

	public String getStyleDojo() {
		return styleDojo;
	}

	public void setJsDojo(String jsDojo) {
		this.jsDojo = jsDojo;
	}

	public void setJsTree(String jsTree) {
		this.jsTree = jsTree;
	}

	public void setLinks(String links) {
		this.links = links;
	}

	public void setStyleCss(String styleCss) {
		this.styleCss = styleCss;
	}

	public void setStyleDojo(String styleDojo) {
		this.styleDojo = styleDojo;
	}
}
